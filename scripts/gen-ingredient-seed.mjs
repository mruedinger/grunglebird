#!/usr/bin/env node
// Generate seed/ingredients.sql from the curated ingredients CSV.
//
//   node scripts/gen-ingredient-seed.mjs <path-to/ingredients.csv> > seed/ingredients.sql
//
// The CSV is the source of truth (handed off out-of-repo); the generated SQL is the
// committed, reproducible artifact. Transform: price dollars -> integer cents,
// YYYY-MM-DD -> unix seconds, SQL single-quote escaping, UTF-8 preserved. Rows use
// INSERT OR IGNORE so re-running is a no-op on existing names (never clobbers edits).
//
// CSV columns: Ingredient, Category, Default unit, Purchase amount, Purchase unit,
//              Purchase price ($), Price updated

import { readFileSync } from 'node:fs';

const csvPath = process.argv[2];
if (!csvPath) {
  console.error('usage: node scripts/gen-ingredient-seed.mjs <path-to/ingredients.csv> > seed/ingredients.sql');
  process.exit(1);
}

/** Minimal CSV line splitter — handles quoted fields with embedded commas. */
function splitCsvLine(line) {
  const out = [];
  let cur = '';
  let inQuotes = false;
  for (let i = 0; i < line.length; i++) {
    const c = line[i];
    if (inQuotes) {
      if (c === '"' && line[i + 1] === '"') { cur += '"'; i++; }
      else if (c === '"') inQuotes = false;
      else cur += c;
    } else if (c === '"') inQuotes = true;
    else if (c === ',') { out.push(cur); cur = ''; }
    else cur += c;
  }
  out.push(cur);
  return out;
}

const sqlStr = (v) => `'${String(v).replace(/'/g, "''")}'`;
const sqlNum = (v) => (v === '' || v == null ? 'NULL' : String(v));

const lines = readFileSync(csvPath, 'utf8').split(/\r?\n/).filter((l) => l.trim() !== '');
lines.shift(); // drop header

const rows = lines.map((line) => {
  const [name, category, defaultUnit, amount, purchaseUnit, priceDollars, updated] =
    splitCsvLine(line).map((f) => f.trim());

  const cents = priceDollars === '' ? null : Math.round(Number(priceDollars) * 100);
  if (cents !== null && !Number.isFinite(cents)) throw new Error(`bad price for ${name}: ${priceDollars}`);

  // Parse the date as UTC midnight so the epoch is deterministic across machines.
  let priceUpdatedAt = null;
  if (updated) {
    const ms = Date.parse(`${updated}T00:00:00Z`);
    if (Number.isNaN(ms)) throw new Error(`bad date for ${name}: ${updated}`);
    priceUpdatedAt = Math.floor(ms / 1000);
  }

  return (
    `INSERT OR IGNORE INTO ingredients ` +
    `(name, category, default_unit, purchase_amount, purchase_unit, purchase_price_cents, price_updated_at) VALUES (` +
    `${sqlStr(name)}, ${category ? sqlStr(category) : 'NULL'}, ${defaultUnit ? sqlStr(defaultUnit) : 'NULL'}, ` +
    `${sqlNum(amount)}, ${purchaseUnit ? sqlStr(purchaseUnit) : 'NULL'}, ${sqlNum(cents)}, ${sqlNum(priceUpdatedAt)});`
  );
});

process.stdout.write(
  `-- Generated by scripts/gen-ingredient-seed.mjs — do not hand-edit.\n` +
    `-- ${rows.length} curated ingredients (issue #20). Re-run via: npm run db:seed:local|remote\n` +
    rows.join('\n') +
    '\n',
);
